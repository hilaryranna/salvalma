    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        if (preco != null && !txtQtd.getText().equals("") && !txtCod.getText().equals("")) {
            txtTotal.setText((preco*Integer.valueOf(txtQtd.getText()))+"");
            compra.add(new String[]{txtCod.getText(), nome, txtQtd.getText(), txtTotal.getText()});
            lblProdAdi.setText(compra.size()+" PRODUTOS ADICIONADOS");

            txtCod.setText("");
            txtQtd.setText("");
            txtTotal.setText("");
            preco = null;
            nome = null;
        } else {
            JOptionPane.showMessageDialog(null, "Selecione ou pesquise um produto e adicione a quantidade dele");
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

