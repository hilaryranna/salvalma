    private void buscarProdutoPorCodigo() {
        String sql = "SELECT * FROM PRODUTO WHERE COD = ?";
        
        if (con == null) 
            con = new ConexaoBd().getConnection();
        try {
            p = con.prepareStatement(sql);
            p.setInt(1, Integer.valueOf(txtCod.getText()));
            
            r = p.executeQuery();
            
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("PRODUTO");
            modelo.addColumn("CODIGO");
            modelo.addColumn("VALOR UN. ou Kg");
            modelo.addColumn("ESTOQUE");
            
            if (r.next()) {
                preco = r.getDouble("PRECO");
                nome = r.getString("NOME");
                modelo.addRow(new Object[]{
                    r.getString("NOME"),
                    r.getInt("COD"),
                    r.getDouble("PRECO"),
                    r.getInt("QTD")
                });
            }
            
            jTable1.setModel(modelo);
            
        } catch (SQLException ex) {
            System.out.println("Erro: "+ex);
        }
    }
    
    private void buscarProdutos() {
        String sql = "SELECT * FROM PRODUTO";
        
        if (con == null) 
            con = new ConexaoBd().getConnection();
        try {
            p = con.prepareStatement(sql);
            r = p.executeQuery();
        
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("PRODUTO");
            modelo.addColumn("CODIGO");
            modelo.addColumn("VALOR UN. ou Kg");
            modelo.addColumn("ESTOQUE");
            
            while (r.next()) {
                modelo.addRow(new Object[]{
                    r.getString("NOME"),
                    r.getInt("COD"),
                    r.getDouble("PRECO"),
                    r.getInt("QTD")
                });
            }
            
            jTable1.setModel(modelo);
            
        } catch (SQLException ex) {
            System.out.println("Erro: "+ex);
        }
    }
