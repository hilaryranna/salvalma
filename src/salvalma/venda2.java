    private void cbCartaoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbCartaoItemStateChanged
        if (cbCartao.isSelected()) {
            if (cbDinheiro.isSelected() && !txtValorDinheiro.getText().equals("")) {
                Double vn = Double.valueOf(txtTotal.getText()) - Double.valueOf(txtValorDinheiro.getText());
                txtValorCartao.setText(vn+"");
            }
            txtValorCartao.setEditable(true);
        } else {
            txtValorCartao.setEditable(false);
        }
    }//GEN-LAST:event_cbCartaoItemStateChanged

    private void btnCalcTrocoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcTrocoActionPerformed
        if (!txtValorDinheiro.getText().equals("") && !txtValorRecebido.getText().equals("")) {
            Double troco = Double.valueOf(txtValorRecebido.getText()) - Double.valueOf(txtValorDinheiro.getText());
            txtTroco.setText(troco+"");
        } else {
            JOptionPane.showMessageDialog(null, "Insira o valor a ser pago em dinheiro e o valor recebido.");
        }
    }//GEN-LAST:event_btnCalcTrocoActionPerformed

    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
        Integer caixa_id = null;
        
        String sql = "SELECT ID FROM CAIXA";
        
        try {
            
            if (con == null)
                con = new ConexaoBd().getConnection();
            
            p = con.prepareStatement(sql);
            r = p.executeQuery();
            
            if (r.last()) {
                caixa_id = r.getInt("ID");
            }
            
            sql = "SELECT MAX(ID) AS idd FROM VENDA";
            p = con.prepareStatement(sql);
            r = p.executeQuery();
            
            int new_id;
            if (r.next()) {
                new_id = r.getInt("idd") + 1;
            } else {
                new_id = 0;
            }
            
            sql = "INSERT INTO VENDA (ID, TOTAL, CAIXA_ID) VALUES (?, ?, ?)";
            p = con.prepareStatement(sql);
            p.setInt(1, new_id);
            p.setDouble(2, Double.valueOf(txtTotal.getText()));
            p.setInt(3, caixa_id);            
            p.executeUpdate();
            
            sql = "INSERT INTO VENDA_PRODUTOS (VENDA_ID, PRODUTO_COD, QTD) VALUES (?, ?, ?)";
            for (String[] s : compra) {
                p = con.prepareStatement(sql);
                p.setInt(1, new_id);
                p.setInt(2, Integer.valueOf(s[0]));
                p.setInt(3, Integer.valueOf(s[2]));  
                p.executeUpdate();
            }
            
            sql = "INSERT INTO VENDA_PAGT (VENDA_ID, FORMA_PAGT_ID, VALOR, TROCO) VALUES (?, ?, ?, ?)";
            if (cbDinheiro.isSelected()) {
                p = con.prepareStatement(sql);
                p.setInt(1, new_id);
                p.setInt(2, 0);
                p.setDouble(3, Double.valueOf(txtValorDinheiro.getText()));  
                p.setDouble(4, Double.valueOf(txtTroco.getText()));  
                p.executeUpdate();
            }
            sql = "INSERT INTO VENDA_PAGT (VENDA_ID, FORMA_PAGT_ID, VALOR, TROCO) VALUES (?, ?, ?, ?)";
            if (cbCartao.isSelected()) {
                p = con.prepareStatement(sql);
                p.setInt(1, new_id);
                p.setInt(2, 1);
                p.setDouble(3, Double.valueOf(txtValorCartao.getText()));
                p.setDouble(4, 0);
                p.executeUpdate();
            }
            
            this.dispose();
        } catch (SQLException ex) {
            System.out.println("Erro: "+ex);
        }
    }//GEN-LAST:event_btnFinalizarActionPerformed

